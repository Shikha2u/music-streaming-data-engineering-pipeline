--  1. CREATE THE DATABASE/ Drops the database if it already exists to ensure a clean start
DROP DATABASE IF EXISTS spotify_db;
CREATE DATABASE spotify_db;

USE spotify_db;

-- - CORE ENTITY TABLES -- -

--  ARTIST Table
CREATE TABLE artist (
    artist_id INT PRIMARY KEY AUTO_INCREMENT,
    artist_first_name VARCHAR(100) NOT NULL,
    artist_last_name VARCHAR(100) NOT NULL,
    country VARCHAR(100),
    bio TEXT,
    genre VARCHAR(100),
    join_date DATE
);


--  SONG Table
CREATE TABLE song (
    song_id INT PRIMARY KEY AUTO_INCREMENT,
    song_name VARCHAR(200) NOT NULL,
    title VARCHAR(200),
    duration INT NOT NULL, --  Duration in seconds
    genre VARCHAR(100),
    release_date DATE,
    CHECK (duration > 0)
);

--  LISTENER_ACCOUNT_TYPE Table (Subscription Details)
CREATE TABLE listener_account_type (
    account_id INT PRIMARY KEY AUTO_INCREMENT,
    account_type VARCHAR(50) NOT NULL,          --  e.g., Free, Individual, Family, Duo, Student
    start_date DATE,
    end_date DATE,
    auto_renew BOOLEAN DEFAULT FALSE
);

--  LISTENER Table (
CREATE TABLE listener (
    listener_id INT PRIMARY KEY AUTO_INCREMENT,
    listener_first_name VARCHAR(100) NOT NULL,
    listener_last_name VARCHAR(100) NOT NULL,
    Date_of_birth DATE,
    gender VARCHAR(50) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    country VARCHAR(100),
    join_date DATE,
    account_id INT NOT NULL,
    FOREIGN KEY (account_id) REFERENCES listener_account_type(account_id)
);

-- - INTERACTION & JUNCTION TABLES -- -

--  SONG_ARTIST Table (Maps Songs to one or more Artists)
CREATE TABLE song_artist (
    song_id INT NOT NULL,
    artist_id INT NOT NULL,
    PRIMARY KEY (song_id, artist_id),
    FOREIGN KEY (song_id) REFERENCES song(song_id),
    FOREIGN KEY (artist_id) REFERENCES artist(artist_id)
);

--  SONG_INTERACTION Table (Records every Listener action on a Song)
CREATE TABLE song_interaction (
    interaction_id INT PRIMARY KEY AUTO_INCREMENT,
    listener_id INT NOT NULL,
    song_id INT NOT NULL,
    interaction_type VARCHAR(50) NOT NULL, --  e.g., Play, Like, Share, Skip
    interaction_timestamp DATETIME NOT NULL,
    FOREIGN KEY (listener_id) REFERENCES listener(listener_id),
    FOREIGN KEY (song_id) REFERENCES song(song_id)
);


--  Create the FOLLOW_ARTIST table
CREATE TABLE follow_artist (
    listener_id INT NOT NULL,
    artist_id INT NOT NULL,
    follow_date DATE NOT NULL, --  The date the listener started following the artist

    --  Define the composite Primary Key using both listener_id and artist_id
    PRIMARY KEY (listener_id, artist_id),

    --  Foreign Key 1: Links to the LISTENER table
    FOREIGN KEY (listener_id) REFERENCES listener(listener_id),

    --  Foreign Key 2: Links to the ARTIST table
    FOREIGN KEY (artist_id) REFERENCES artist(artist_id)
);





